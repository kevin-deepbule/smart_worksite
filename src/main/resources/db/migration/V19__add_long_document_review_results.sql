CREATE TABLE review_document_chunk (
  id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT 'Primary key ID',
  project_id BIGINT NOT NULL COMMENT 'Project ID',
  review_record_id BIGINT NOT NULL COMMENT 'Review record ID',
  parse_record_id BIGINT NOT NULL COMMENT 'File parse record ID',
  chunk_no INT NOT NULL COMMENT 'Chunk sequence starting from 1',
  chunk_code VARCHAR(64) NOT NULL COMMENT 'Stable chunk code',
  heading_path VARCHAR(1000) NULL COMMENT 'Markdown heading path',
  page_start INT NULL COMMENT 'Start page number',
  page_end INT NULL COMMENT 'End page number',
  sheet_name VARCHAR(255) NULL COMMENT 'Excel sheet name',
  row_start INT NULL COMMENT 'Excel start row',
  row_end INT NULL COMMENT 'Excel end row',
  content_object_name VARCHAR(500) NOT NULL COMMENT 'Chunk content object name',
  content_hash VARCHAR(128) NOT NULL COMMENT 'Chunk content SHA-256',
  char_count INT NOT NULL DEFAULT 0 COMMENT 'Chunk character count',
  token_count INT NULL COMMENT 'Estimated token count',
  status VARCHAR(32) NOT NULL DEFAULT 'READY' COMMENT 'READY or FAILED',
  error_message TEXT NULL COMMENT 'Chunk failure message',
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Created time',
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT 'Updated time',
  created_by BIGINT NULL COMMENT 'Created by user ID',
  updated_by BIGINT NULL COMMENT 'Updated by user ID',
  deleted TINYINT NOT NULL DEFAULT 0 COMMENT 'Logical delete flag',
  UNIQUE KEY uk_review_chunk (review_record_id, chunk_no, deleted),
  KEY idx_review_chunk_project (project_id),
  KEY idx_review_chunk_review (review_record_id),
  KEY idx_review_chunk_parse (parse_record_id),
  KEY idx_review_chunk_hash (content_hash),
  KEY idx_review_chunk_deleted (deleted)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='Review Markdown chunk metadata table';

CREATE TABLE review_rule_result (
  id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT 'Primary key ID',
  project_id BIGINT NOT NULL COMMENT 'Project ID',
  review_record_id BIGINT NOT NULL COMMENT 'Review record ID',
  template_id BIGINT NOT NULL COMMENT 'Template ID snapshot',
  template_file_id BIGINT NULL COMMENT 'Template file ID snapshot',
  rule_code VARCHAR(128) NOT NULL COMMENT 'Review rule code',
  rule_description TEXT NOT NULL COMMENT 'Review rule description snapshot',
  rule_order INT NOT NULL DEFAULT 0 COMMENT 'Rule order',
  execution_status VARCHAR(32) NOT NULL DEFAULT 'PENDING' COMMENT 'PENDING, RUNNING, SUCCESS or FAILED',
  compliance_status VARCHAR(32) NULL COMMENT 'COMPLIANT, NON_COMPLIANT or PARTIALLY_COMPLIANT',
  reason TEXT NULL COMMENT 'Review conclusion reason',
  suggestion TEXT NULL COMMENT 'Remediation suggestion',
  evidence_json JSON NULL COMMENT 'Effective evidence with chunk references',
  confidence DECIMAL(5,4) NULL COMMENT 'Confidence from 0 to 1',
  model_trace_id VARCHAR(128) NULL COMMENT 'Python model trace ID',
  error_message TEXT NULL COMMENT 'Rule execution failure message',
  started_at DATETIME NULL COMMENT 'Rule execution started time',
  completed_at DATETIME NULL COMMENT 'Rule execution completed time',
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Created time',
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT 'Updated time',
  created_by BIGINT NULL COMMENT 'Created by user ID',
  updated_by BIGINT NULL COMMENT 'Updated by user ID',
  deleted TINYINT NOT NULL DEFAULT 0 COMMENT 'Logical delete flag',
  UNIQUE KEY uk_review_rule (review_record_id, rule_code, deleted),
  KEY idx_review_rule_project (project_id),
  KEY idx_review_rule_review (review_record_id),
  KEY idx_review_rule_execution (execution_status),
  KEY idx_review_rule_compliance (compliance_status),
  KEY idx_review_rule_deleted (deleted)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='Review rule snapshot and final result table';
