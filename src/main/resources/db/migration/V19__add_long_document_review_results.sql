  ADD COLUMN parse_record_id BIGINT NULL COMMENT 'File parse record ID' AFTER task_id,
  ADD COLUMN current_stage VARCHAR(32) NULL COMMENT 'Current review stage' AFTER status,
  ADD COLUMN progress INT NOT NULL DEFAULT 0 COMMENT 'Review progress 0-100' AFTER current_stage,
  ADD COLUMN overall_status VARCHAR(32) NULL COMMENT 'COMPLIANT, NON_COMPLIANT or PARTIALLY_COMPLIANT' AFTER progress,
  ADD COLUMN summary MEDIUMTEXT NULL COMMENT 'Final review summary' AFTER overall_status,
  ADD COLUMN rule_total INT NOT NULL DEFAULT 0 COMMENT 'Total rule count' AFTER summary,
  ADD COLUMN rule_completed INT NOT NULL DEFAULT 0 COMMENT 'Completed rule count' AFTER rule_total,
  ADD COLUMN chunk_total INT NOT NULL DEFAULT 0 COMMENT 'Document chunk count' AFTER rule_completed,
  ADD COLUMN started_at DATETIME NULL COMMENT 'Review started time' AFTER error_message,
  ADD COLUMN completed_at DATETIME NULL COMMENT 'Review completed time' AFTER started_at,
